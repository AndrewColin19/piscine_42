int	ft_sqrt(int nb)
{
	unsigned int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	while (i * i < (unsigned int)nb)
		i++;
	if ((nb % i) == 0)
		return ((int)i);
	else
		return (0);
}
