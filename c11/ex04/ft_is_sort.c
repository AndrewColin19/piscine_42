int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	i;
	int	a;
	int	b;

	a = 1;
	b = 1;
	i = 0;
	while (i < length - 1)
	{
		if (f(tab[i], tab[i + 1]) > 0)
			a = 0;
		else if (f(tab[i], tab[i + 1]) < 0)
			b = 0;
		i++;
	}
	return (a || b);
}
